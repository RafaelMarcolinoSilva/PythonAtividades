print("Cálculo de média aritmética")#título do programa
num1=int(input("Insira o primeiro Número:\n" ))#entrada do primeiro e demais números
num2=int(input("Insira o segundo Número:\n" ))
num3=int(input("Insira o terceiro Número:\n" ))
num4=int(input("Insira o quarto Número:\n" ))

media= (num1+num2+num3+num4) / 4 #fórmula que realiza o cálculo de média
print(f"O resultado da média aritmética é {media}!!!") #impressão do resultado do cálculo da média